    private String imprimirLista(Lista[] lista){
        int i,j, tamanho;
        String resul = "Vertice\tCaminho\n";
        for(i=0; i<Grafos.tam; i++){
            tamanho = lista[i].size();
            resul += String.valueOf(i) + "|\t";
            for(j=0 ; j<tamanho; j++){
                resul+= String.valueOf(lista[i].getVer(j))+ "->";
            }
            resul+="/\n";
        }
        return resul;
    }
