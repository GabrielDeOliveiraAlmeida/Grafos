    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        inicial();
        int inicial;
        int[] vet = new int[Grafos.tam];
        String impressao;
        if (!repres()) {
            return;
        }
        try {
            inicial = Integer.parseInt(verticeInicialB.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Entrada inválida","ERRO", ERROR_MESSAGE);
            verticeInicialB.setText("");
            verticeInicialB.requestFocus();
            return;
        }
        if (!control.verificarEntrada(inicial)) {
            JOptionPane.showMessageDialog(null, "Entrada inválida","ERRO", ERROR_MESSAGE);
            resetCampo(verticeInicialB);
            return;
        }
        if (!control.verificarEntrada(inicial)) {
            return;
        }
        if (radioProfundidade.isSelected()) {
            impressao = control.buscaProfundidadeString(inicial, representacao);
            resultadoText.setText(impressao);
        } else {
            impressao = control.buscaLarguraString(vet, inicial, representacao);
            view.pintarArestas(vet);
            resultadoText.setText(impressao);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        inicial();
        if (!repres()) {
            return;
        }
        int inicio = 0, fim, tam;
        Boolean existe;
        String cami;
        String impressao = "";
        ArrayList<String> lista = new ArrayList<>();
        CardLayout c = (CardLayout) root.getLayout();
        try {
            inicio = Integer.parseInt(inicialVer.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Entrada inválida","ERRO", ERROR_MESSAGE);
            resetCampo(inicialVer);
            return;
        }
        try {
            fim = Integer.parseInt(finalVer.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Entrada inválida","ERRO", ERROR_MESSAGE);
            resetCampo(finalVer);
            return;
        }
        if (!(control.verificarEntrada(inicio) && control.verificarEntrada(fim))) {
            JOptionPane.showMessageDialog(null, "Entrada inválida ","ERRO", ERROR_MESSAGE);
            resetCampo(finalVer);
            resetCampo(inicialVer);
            return;
        }
        if (radioProfundidadeV.isSelected()) {
            //impressao += control.buscaProfundidadeString(inicio, representacao);
            lista = control.verificarCaminho(inicio, fim, representacao, true);
            if (lista != null) {
                view.caminho(lista);
                cami = String.join("-> ", lista);
                cami = impressao + "Busca Profundidade:\n" + cami;
                resultadoText.setText(cami);
            } else {
                cami = "Busca Largura:\n" + "Caminho Não Encontrado";
                resultadoText.setText(cami);
            }
        } else {
            lista = control.verificarCaminho(inicio, fim, representacao, false);
            if (lista != null) {
                view.caminho(lista);
                cami = String.join("-> ", lista);
                cami = "Busca Largura:\n" + cami;
                resultadoText.setText(cami);
            } else {
                cami = "Busca Largura:\n" + "Caminho Não Encontrado";
                resultadoText.setText(cami);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

